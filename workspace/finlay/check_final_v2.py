def get_tax_year(d):
    # UK Tax Year: April 6th to April 5th of the following year
    if (d.month > 4) or (d.month == 4 and d.day >= 6):
        return f"{d.year}_{d.year+1}"
    else:
        return f"{d.year-1}_{d.year}"

